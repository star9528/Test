
//加上abstract关键字之后，Shape这个类就成了抽象类
//抽象类和普通类相比，大部分都是一样的（可以有属性，可以有方法，
// 可以有用静态方法，也可以继承别的类
//最主要的区别在于，抽象类，无法实例化（无法new一个对应类型的对象）
//抽象类中还可以包含抽象方法，而普通类无法包含抽象方法
abstract public class Shape {
    //此处这个draw方法也非常抽象，把它也设定成抽象方法
    //抽象发法，使用abstract关键字描述，同时，抽象方法没有方法体
    //抽象类和抽象方法存在的意义就是被其他的类继承和重写抽象发法
    abstract public void draw();
}
